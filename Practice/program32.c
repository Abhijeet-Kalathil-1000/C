//////////////////////////////////////////////////////////////////////////////////
//
//  Function Name       :   DisplaySumOfFactors
//  Description         :   Displays Sum of factors of given decimal number
//  Input               :   Integer
//  Output              :   Integer
//  Author              :   Abhijeet Saji Kalathil
//  Date                :   25/04/2022
//
//////////////////////////////////////////////////////////////////////////////////

int DisplaySumOfFactors(int iNo)
{
    int iCnt = 0;
    int iSum = 0;

//////////////////////////////////////////////////////////////////////////////////
//  Updater : to convert negative input
//////////////////////////////////////////////////////////////////////////////////

    if(iNo < 0)         
    {
        iNo = -iNo;
    }
//////////////////////////////////////////////////////////////////////////////////
//  Time complexity : N/2
//////////////////////////////////////////////////////////////////////////////////

    for(iCnt = 1;iCnt <=(iNo/2);iCnt++)
        {
            if ((iNo % iCnt) == 0)
            {
                printf("%d \n",iCnt);
                iSum = iSum + iCnt;
            }
        }
    return iSum;
}

//////////////////////////////////////////////////////////////////////////////////
//Accept number from user and display its factors
//////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;
        
    printf("Enter a number to check its factors : ");
    scanf("%d",&iValue);

    iRet = DisplaySumOfFactors(iValue);
    printf("%d is sum of all factors\n",iRet);

    return 0;
}

